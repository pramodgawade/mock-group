package stringclassmethod;

public class substring {

	public static void main(String[] args) {

		
		String s1="their was one big dog";
		
		System.out.println(s1.substring(6));
		
		
		String s2="in my organization i am working as qa";
		
		System.out.println(s2.substring(19));
		
		
		System.out.println(s1.substring(0, 9));
	}

}
